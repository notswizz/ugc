import { useState, useEffect } from 'react';
import { useRouter } from 'next/router';
import { useAuth } from '@/lib/auth/AuthContext';
import { Button } from '@/components/ui/button';
import { Card, CardContent } from '@/components/ui/card';
import AnimatedLogo from '@/components/branding/AnimatedLogo';
import { doc, getDoc } from 'firebase/firestore';
import { db } from '@/lib/firebase/client';

export default function Signup() {
  const [isLoading, setIsLoading] = useState(false);
  const { signInWithGoogle, user, appUser, loading } = useAuth();
  const router = useRouter();

  // Check if user is already logged in and redirect
  useEffect(() => {
    if (!loading && user && appUser) {
      checkOnboardingAndRedirect();
    }
  }, [user, appUser, loading]);

  const checkOnboardingAndRedirect = async () => {
    if (!user || !appUser) return;

    try {
      // Check if user has completed onboarding by checking for brand/creator profile
      if (appUser.role === 'brand') {
        const brandDoc = await getDoc(doc(db, 'brands', user.uid));
        if (brandDoc.exists()) {
          router.push('/brand/dashboard');
          return;
        }
      } else if (appUser.role === 'creator') {
        const creatorDoc = await getDoc(doc(db, 'creators', user.uid));
        if (creatorDoc.exists()) {
          router.push('/creator/dashboard');
          return;
        }
      }

      // If no profile exists, go to role selection
      if (appUser.role) {
        router.push('/onboarding/role');
      } else {
        router.push('/onboarding/role');
      }
    } catch (error) {
      console.error('Error checking onboarding:', error);
      // Fallback to role selection on error
      router.push('/onboarding/role');
    }
  };

  const handleGoogleLogin = async () => {
    setIsLoading(true);
    try {
      await signInWithGoogle();
      // The useEffect will handle redirect after auth state updates
    } catch (error) {
      // Error is handled in AuthContext
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <div className="min-h-screen flex items-center justify-center bg-gradient-to-br from-orange-50 via-white to-orange-50 px-4 py-12">
      <div className="w-full max-w-md space-y-6">
        {/* Logo and branding */}
        <div className="text-center space-y-3">
          <div className="flex flex-col items-center gap-3">
            <div className="flex justify-center">
              <AnimatedLogo size="xl" />
            </div>
            <img 
              src="/logotext.PNG" 
              alt="UGC Dash" 
              className="h-14 md:h-16 w-auto"
            />
          </div>
        </div>

        {/* Login card */}
        <Card className="border-0 shadow-xl">
          <CardContent className="pt-8 pb-6 space-y-5">
            <Button
              onClick={handleGoogleLogin}
              disabled={isLoading}
              className="w-full h-14 text-base font-medium bg-white hover:bg-gray-50 text-gray-900 border-2 border-gray-300 shadow-sm hover:shadow-md transition-all"
              variant="outline"
            >
              {isLoading ? (
                <div className="flex items-center gap-2">
                  <div className="w-5 h-5 border-2 border-orange-600 border-t-transparent rounded-full animate-spin" />
                  <span>Signing in...</span>
                </div>
              ) : (
                <div className="flex items-center gap-3">
                  <svg width="18" height="18" viewBox="0 0 24 24" className="w-5 h-5">
                    <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
                    <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
                    <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
                    <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
                  </svg>
                  <span>Continue with Google</span>
                </div>
              )}
            </Button>

            <p className="text-center text-xs text-gray-500">
              By continuing, you agree to UGC Dash's{' '}
              <a href="#" className="text-orange-600 hover:underline">Terms</a>
              {' '}and{' '}
              <a href="#" className="text-orange-600 hover:underline">Privacy Policy</a>
            </p>
          </CardContent>
        </Card>

        {/* Features highlight */}
        <div className="flex justify-center gap-8 text-center">
          <div className="space-y-1">
            <div className="text-2xl">âš¡</div>
            <p className="text-xs text-gray-600">Fast</p>
          </div>
          <div className="space-y-1">
            <div className="text-2xl">ðŸ’°</div>
            <p className="text-xs text-gray-600">Fair</p>
          </div>
          <div className="space-y-1">
            <div className="text-2xl">ðŸŽ¯</div>
            <p className="text-xs text-gray-600">Trust</p>
          </div>
        </div>
      </div>
    </div>
  );
}
